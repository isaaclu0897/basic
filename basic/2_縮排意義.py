# print absolate vaule of integer
a = -20

if a >= 0:
    print(a)
else:
    print(-a)
    
''' 說明
1. # 是註釋。
2. : 結尾，縮排得語句為代碼塊。
3. 縮排不成文規定為"4個空格"。
4. 縮排的好處是強迫寫出更有邏輯性的、可讀性的文件，並減少縮排。
5. 縮排的壞處是複製、貼上代碼的功能失效了，必須重新檢驗縮排。
'''