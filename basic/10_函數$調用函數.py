#==============================================================================
# bytes([source[, encoding[, errors]]]) 將 source 轉換為 bytes ，若 source 為字串，需提供 encoding ，也就是字串的編碼格式
# bytearray([source[, encoding[, errors]]])	 將 source 轉換為 bytes ，若 source 為字串，需提供 encoding ，也就是字串的編碼格式
#==============================================================================

d1 = "!@#"
print(type(d1))
d2 = bytes(d1, "utf8")
print(type(d2)) 
print(d2)
for i in d2:
    print(i)
 
d3 = bytes(d1, "ascii")
print(d3)
for i in d3:
    print(i)
    
d1 = b"12345"
print(type(d1))
d2 = bytearray(d1)
print(type(d2)) 
print(d2)
d2[0] = 54   
print(d2)
d2[1] = 55
print(d2) 
d2[2] = 56
print(d2)
d2[3] = 57
print(d2)
d2[4] = 58
print(d2)

