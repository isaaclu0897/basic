print(pow(2, 8))
print(pow(2, 9, 3))
print(pow(2, 9, 4))
print(pow(2, 9, 5))
print(pow(2, 9, 6))

#==============================================================================
# range([start], stop[, step])	建立整數序列
#==============================================================================
print(range(10))
print(type(range(10)))
print(list(range(10)))
print(list(range(0, 10)))
print(list(range(0, 10, 2)))
print(list(range(9, -1, -1)))

